<script setup lang="ts">
import {
  computed,
  useSlots,
} from 'vue';
import type { PropType } from 'vue';
import equal from 'fast-deep-equal';
import { uid } from 'uid/single';
import type { PropsAttrs } from '../../../types/attrs';
import type { Icon } from '../../../types/icon';
import type { HTMLTag } from '../../../types/tag';
import UiIcon from '../UiIcon/UiIcon.vue';
import UiText from '../UiText/UiText.vue';
import useAttributes from '../../../composable/useAttributes';
import { keyboardFocus as vKeyboardFocus } from '../../../utilities/directives';

export type CheckboxValue = string | Record<string, unknown>[];
export type CheckboxModelValue = boolean | Record<string, unknown>[];
const props = defineProps({
  /**
   *  Use this props or v-model to set checked.
   */
  modelValue: {
    type: [
      Boolean,
      Array,
    ] as PropType<CheckboxModelValue>,
    default: false,
  },
  /**
   * Use this props to set value of checkbox.
   * Required for multiple checkboxes.
   */
  value: {
    type: [
      String,
      Object,
    ] as PropType<CheckboxValue>,
    default: '',
  },
  /**
   * Use this props to set checkbox id.
   * Default value is generated by uid i.e. `checkbox-<uid>`.
   */
  id: {
    type: String,
    default: '',
  },
  /**
   * Use this props to disabled checkbox.
   * Remember to use `ui-checkbox--is-disabled` class to style disabled checkbox.
   */
  disabled: {
    type: Boolean,
    default: false,
  },
  /**
   * Use this props to pass attrs for input element.
   */
  inputAttrs: {
    type: Object as PropsAttrs,
    default: () => ({}),
  },
  /**
   * Use this props to pass attrs for checkmark UiIcon
   */
  iconCheckmarkAttrs: {
    type: Object as PropsAttrs,
    default: () => ({ icon: 'checkmark' }),
  },
  /**
   * Use this props to pass attrs for label UiText
   */
  textLabelAttrs: {
    type: Object as PropsAttrs,
    default: () => ({ tag: 'span' }),
  },
});
const {
  attrs, listeners,
} = useAttributes();
const defaultProps = computed(() => ({
  iconCheckmarkAttrs: {
    icon: 'checkmark' as Icon,
    ...props.iconCheckmarkAttrs,
  },
  textLabelAttrs: {
    tag: 'span' as HTMLTag,
    ...props.textLabelAttrs,
  },
  inputAttrs: {
    disabled: props.disabled,
    ...listeners.value,
    ...props.inputAttrs,
  },
}));
const emit = defineEmits<{(e: 'update:modelValue', value: CheckboxModelValue): void
}>();
const slots = useSlots();
const hasLabel = computed(() => (!!slots.default));
const checkboxId = computed(() => (props.id || `checkbox-${uid()}`));
const isChecked = computed(() => {
  if (Array.isArray(props.modelValue)) {
    return !!props.modelValue.find((option) => (
      equal(JSON.parse(JSON.stringify(props.value)), JSON.parse(JSON.stringify(option)))));
  }
  return props.modelValue;
});
const getChecked = (checked: boolean): CheckboxModelValue => {
  if (Array.isArray(props.modelValue)) {
    return checked
      ? [
        ...props.modelValue,
        JSON.parse(JSON.stringify(props.value)),
      ]
      : props.modelValue.filter((option: Record<string, unknown>) => (!equal(props.value, option)));
  }
  return checked;
};
function changeHandler(event: Event): void {
  const el = event.target as HTMLInputElement;
  emit('update:modelValue', getChecked(el.checked));
}

// TODO: remove in 0.6.0 / BEGIN
const checkbutton = computed(() => (slots.checkbutton));
if (checkbutton.value) {
  if (process.env.NODE_ENV === 'development') {
    console.warn('[@infermedica/component-library warn][UiCheckbox]: The `checkbutton` slot will be removed in 0.6.0. Please use `checkbox` slot instead.');
  }
}
// END
</script>


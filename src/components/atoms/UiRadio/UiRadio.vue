<template>
  <label
    class="ui-radio"
    :for="radioId"
    v-bind="{
      ...attrs,
      ...elementsListeners.label
    }"
  >
    <input
      :id="radioId"
      ref="input"
      v-keyboard-focus
      type="radio"
      class="visual-hidden"
      :checked="isChecked"
      v-bind="defaultProps.inputAttrs"
      @change="changeHandler($event)"
    >
    <!-- @slot Use this slot to replace radiobutton template. -->
    <slot
      name="radio"
      v-bind="{
        checked: isChecked,
        radioElementAttrs,
      }"
    >
      <div
        v-bind="radioElementAttrs"
        :class="[
          'ui-radio__radio',{ 'ui-radio__radio--is-checked': isChecked }
        ]"
      >
        <div class="ui-radio__mark" />
      </div>
    </slot>
    <!-- @slot Use this slot to replace label template. -->
    <slot
      name="label"
      v-bind="{
        hasLabel,
        textLabelAttrs: defaultProps.textLabelAttrs
      }"
    >
      <UiText
        v-if="hasLabel"
        v-bind="defaultProps.textLabelAttrs"
        class="ui-radio__label"
      >
        <!-- @slot Use this slot to place content inside label. -->
        <slot />
      </UiText>
    </slot>
  </label>
</template>

<script lang="ts">
export default { inheritAttrs: false };
</script>

<script setup lang="ts">
import {
  ref,
  computed,
  useSlots,
} from 'vue';
import type {
  HTMLAttributes,
  InputHTMLAttributes,
  LabelHTMLAttributes,
} from 'vue';
import equal from 'fast-deep-equal';
import { uid } from 'uid/single';
import useAttributes from '../../../composable/useAttributes';
import { keyboardFocus as vKeyboardFocus } from '../../../utilities/directives';
import UiText from '../UiText/UiText.vue';
import type { TextAttrsProps } from '../UiText/UiText.vue';
import type { DefineAttrsProps } from '../../../types';

export type RadioModelValue = number | string | Record<string, unknown>;
export interface RadioProps {
  /**
   * Use this props or v-model to set checked.
   */
  modelValue?: RadioModelValue;
  /**
   * Use this props to set value of radio.
   */
  value?: RadioModelValue;
  /**
   * Use this props to set radio id
   * Default value is generated by uid i.e. `radio-<uid>`.
   */
  id?: string;
  /**
   * Use this props to disabled input.
   * Remember to use `ui-radio--is-disabled` class to style disabled radio.
   */
  disabled?: boolean;
  /**
   * Use this props to pass attrs for input element.
   */
  inputAttrs?: DefineAttrsProps<null, InputHTMLAttributes>;
  /**
   * Use this props to pass attrs for radio element.
   */
  radioElementAttrs?: DefineAttrsProps<null, HTMLAttributes>;
  /**
   * Use this props to pass attrs for label UiText
   */
  textLabelAttrs?: TextAttrsProps;
}
export type RadioAttrsProps = DefineAttrsProps<RadioProps, LabelHTMLAttributes>;
export interface RadioEmits {
  (e: 'update:modelValue', value: RadioModelValue): void;
}

const props = withDefaults(defineProps<RadioProps>(), {
  modelValue: '',
  value: '',
  id: '',
  disabled: false,
  inputAttrs: () => ({}),
  radioElementAttrs: () => ({}),
  textLabelAttrs: () => ({ tag: 'span' }),
});
const defaultProps = computed(() => {
  const tag: TextAttrsProps['tag'] = 'span';
  return {
    textLabelAttrs: {
      tag,
      ...props.textLabelAttrs,
    },
    inputAttrs: {
      disabled: props.disabled,
      ...elementsListeners.value.input,
      ...props.inputAttrs,
    },
  };
});
const emit = defineEmits<RadioEmits>();
const slots = useSlots();
const {
  attrs, listeners,
} = useAttributes<LabelHTMLAttributes>();
const elementsListeners = computed(() => {
  const {
    onFocus, onBlur, ...rest
  } = listeners.value;
  return {
    input: {
      onFocus,
      onBlur,
    },
    label: rest,
  };
});
const hasLabel = computed(() => (!!slots.default));
const radioId = computed(() => (
  props.id || `radio-${uid()}`
));
const isChecked = computed(() => (equal(
  JSON.parse(JSON.stringify(props.value)),
  JSON.parse(JSON.stringify(props.modelValue)),
)));
const changeHandler = (event: Event) => {
  const el = event.target as HTMLInputElement;
  if (el.checked) {
    emit('update:modelValue', JSON.parse(JSON.stringify(props.value)));
  }
};
const input = ref(null);
</script>

